people = 20
cats   = 30
dogs   = 15

if people < cats
	puts "too many cats, the world is doomed"
end

if people > cats
	puts "not many cats, the world is safe"
end

if people < dogs
	puts "the world is drooled on"
end

if people > dogs
	puts "the world is dry"
end

if people >= dogs
	puts "people are less than or equal to dogs"
end

if people == dogs
	puts "people are dogs"
end
